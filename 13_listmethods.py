#some list methods
a=[1,7,9,45,5,3,2,6,10]
a.sort() #to sort the list
print(a)
a.reverse() #to reverse the list
print(a)
a.append(26) #to add value in list at the last
print(a)
a.insert(0,67) #this will add 67 at the 0th location of thelist
print(a)
a.pop(4) #this will remove 4th index value
print(a)